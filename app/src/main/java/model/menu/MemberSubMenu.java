package model.menu;

/**
 * Represents the sub-menu for member-related actions in the application.
 */
public class MemberSubMenu {
  /**
   * Enumeration of choices available in the member sub-menu.
   */
  public enum MemberSubMenuChoice {
    DELETE_MEMBER,
    UPDATE_MEMBER,
    FULL_INFO,
    ADD_ITEM,
    DELETE_ITEM,
    BORROW_ITEM,
    RETURN,
    INVALID;
  }
}
