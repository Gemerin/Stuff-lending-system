package model.menu;

/**
 * Represents the sub-menu for item-related actions in the application.
 */
public class ItemSubMenu {
  /**
   * Enumeration of choices available in the item sub-menu.
   */
  public enum ItemSubMenuChoice {
    DELETE_ITEM,
    UPDATE_ITEM,
    ITEM_INFORMATION,
    ITEM_CONTRACTS,
    RETURN,
    INVALID;
  }
}
